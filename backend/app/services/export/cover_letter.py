async def generate_cover_letter(
    user: Any,
    year: int,
    summary: dict[str, Any],
    db: Any,
) -> bytes:
    """Generate cover letter PDF bytes.

    Args:
        user: User ORM model (may have decrypted full_name, inn fields)
        year: Tax year
        summary: dict with keys total_amount, months (list)
        db: AsyncSession (unused currently, reserved for future queries)

    Returns:
        PDF bytes

    Raises:
        RuntimeError if neither WeasyPrint nor ReportLab is available
    """
    # Extract fields from user (may be None / encrypted placeholder)
    full_name: str = getattr(user, "full_name", "") or ""
    inn: str = getattr(user, "inn", "") or ""

    total_amount = Decimal(str(summary.get("total_amount", "0")))
    months = summary.get("months", [])
    receipts_count = sum(int(m.get("receipts_count", 0)) for m in months)

    # Count prescriptions (approximate from summary)
    prescriptions_count: int = summary.get("prescriptions_count", 0)

    html = _build_html(full_name, inn, year, total_amount, receipts_count, prescriptions_count)

    if _HAS_WEASYPRINT:
        pdf_bytes: bytes = _WP_HTML(string=html).write_pdf()
        logger.debug("Cover letter generated via WeasyPrint (%d bytes)", len(pdf_bytes))
        return pdf_bytes

    if _HAS_REPORTLAB:
        return _reportlab_fallback(full_name, inn, year, total_amount, receipts_count)

    raise RuntimeError("Neither WeasyPrint nor ReportLab is installed")


def _reportlab_fallback(
    full_name: str,
    inn: str,
    year: int,
    total_amount: Decimal,
    receipts_count: int,
) -> bytes:
    """Minimal ReportLab-based cover letter when WeasyPrint is unavailable."""
    import os

    buf = _io.BytesIO()
    # Try to register Cyrillic font
    font_name = "Helvetica"
    try:
        import reportlab

        rl_dir = os.path.dirname(reportlab.__file__)
        font_path = os.path.join(rl_dir, "fonts", "DejaVuSans.ttf")
        if os.path.exists(font_path):
            _pdfmetrics.registerFont(_TTFont("DejaVuSans", font_path))
            font_name = "DejaVuSans"
    except Exception:
        pass

    doc = _Doc(
        buf,
        pagesize=_A4,
        leftMargin=3 * _cm,
        rightMargin=2.5 * _cm,
        topMargin=2 * _cm,
        bottomMargin=2 * _cm,
        title=f"Заявление на вычет {year}",
    )
    styles = _get_styles()
    normal = styles["Normal"]
    normal.fontName = font_name
    normal.fontSize = 11
    normal.leading = 16

    ndfl = round(float(total_amount) * 0.13, 2)
    story = [
        _Para(f"<b>Заявление о налоговом вычете {year} года</b>", normal),
        _Spacer(1, 0.5 * _cm),
        _Para(f"ФИО: {full_name or '___________'}", normal),
        _Para(f"ИНН: {inn or '___________'}", normal),
        _Spacer(1, 0.5 * _cm),
        _Para(
            f"Прошу предоставить социальный налоговый вычет (ст. 219 НК РФ) "
            f"по расходам на лекарства за {year} год.",
            normal,
        ),
        _Spacer(1, 0.3 * _cm),
        _Para(f"Сумма расходов: {_fmt_rub(total_amount)} руб.", normal),
        _Para(f"Возврат НДФЛ (13%): {_fmt_rub(ndfl)} руб.", normal),
        _Spacer(1, 0.3 * _cm),
        _Para(f"Приложения: {receipts_count} чеков, реестр расходов.", normal),
        _Spacer(1, 1 * _cm),
        _Para("Дата: ___________  Подпись: ___________", normal),
    ]
    doc.build(story)
    return buf.getvalue()
