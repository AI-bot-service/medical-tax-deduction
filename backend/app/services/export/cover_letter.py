"""Cover Letter PDF Generator (H-02).

Generates a WeasyPrint PDF cover letter for IFNS submission under ст. 219 НК РФ.

The letter contains:
  - Addressee block (tax authority — blank field for user to fill)
  - Applicant: ФИО, ИНН, address (placeholder)
  - Statement text referencing ст. 219 НК РФ
  - List of attached documents (N receipts, M prescriptions, PDF registry)
  - Date and signature line

Fallback: if WeasyPrint is not installed, returns a minimal plaintext-inside-PDF
generated by reportlab (if available), so the ZIP always contains some cover letter.
"""
from __future__ import annotations

import logging
import uuid
from decimal import Decimal
from typing import Any

logger = logging.getLogger(__name__)

# ── WeasyPrint ────────────────────────────────────────────────────────────────
try:
    from weasyprint import HTML as _WP_HTML

    _HAS_WEASYPRINT = True
except ImportError:
    _HAS_WEASYPRINT = False

# ── ReportLab fallback ────────────────────────────────────────────────────────
try:
    import io as _io

    from reportlab.lib.pagesizes import A4 as _A4
    from reportlab.lib.styles import getSampleStyleSheet as _get_styles
    from reportlab.lib.units import cm as _cm
    from reportlab.pdfbase import pdfmetrics as _pdfmetrics
    from reportlab.pdfbase.ttfonts import TTFont as _TTFont
    from reportlab.platypus import (
        Paragraph as _Para,
        SimpleDocTemplate as _Doc,
        Spacer as _Spacer,
    )

    _HAS_REPORTLAB = True
except ImportError:
    _HAS_REPORTLAB = False


