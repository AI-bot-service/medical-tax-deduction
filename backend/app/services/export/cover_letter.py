def _fmt_rub(amount: Decimal | float | str) -> str:
    return f"{float(amount):,.2f}".replace(",", " ").replace(".", ",")


def _build_html(
    full_name: str,
    inn: str,
    year: int,
    total_amount: Decimal,
    receipts_count: int,
    prescriptions_count: int,
) -> str:
    ndfl = float(total_amount) * 0.13
    doc_rows_items = [
        (1, f"Реестр расходов на лекарственные препараты за {year} год — 1 экз."),
        (2, f"Чеки (кассовые чеки) из аптек — {receipts_count} шт."),
    ]
    if prescriptions_count > 0:
        doc_rows_items.append(
            (3, f"Рецепты, оформленные в установленном порядке — {prescriptions_count} шт.")
        )
    doc_rows_items.append(
        (
            len(doc_rows_items) + 1,
            "Справка из медицинского учреждения (при необходимости)",
        )
    )
    rows_html = "\n".join(
        f"<tr><td>{i}.</td><td>{text}</td></tr>" for i, text in doc_rows_items
    )

    # Short name: "Иванов И.И."
    parts = full_name.split()
    if len(parts) >= 2:
        short = parts[0] + " " + " ".join(p[0] + "." for p in parts[1:])
    else:
        short = full_name

    return _HTML_TEMPLATE.format(
        full_name=full_name or "___________________",
        inn=inn or "___________________",
        year=year,
        total_amount=_fmt_rub(total_amount),
        ndfl_return=_fmt_rub(ndfl),
        doc_rows=rows_html,
        full_name_short=short or "___________________",
    )


